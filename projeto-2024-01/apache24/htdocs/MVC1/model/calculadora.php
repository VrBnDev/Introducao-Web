<?php

class Calculadora {
	
	public static function somar($n1, $n2){
		return $n1 + $n2;
	}

	public static function subtrair($n1, $n2){
		return $n1 - $n2;
	}

	public static function multiplicar($n1, $n2){
		return $n1 * $n2;
	}

	public static function dividir($n1, $n2){
		return $n1 / $n2;
	}

}

?>